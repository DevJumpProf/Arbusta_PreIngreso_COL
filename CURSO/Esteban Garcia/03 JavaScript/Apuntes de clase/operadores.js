//de asignacion
let numero = 10; // = es el operador de asignacion

//aritmeticos
let suma = 10 + 10; // + es el operador de suma
let resta = 10 - 5; // - es el operador de resta
let multiplicacion = 10 * 10; // * es el operador de multiplicacion
let division = 10 / 2; // / es el operador de division

//modulo o residuo
let residuo = 10 % 3; // % es el resto de la division

//mas operadores aritmeticos
let incremento = 10++; // ++ es el operador de incremento en 1	
let decremento = 10--; // -- es el operador de decremento en 1

//comparacion simple
let esIgual = 10 == 10; // == es el operador de comparacion simple, devuelve un valor booleano
let esDiferente = 10 != 10; // != es el operador de comparacion simple, devuelve un valor booleano

//comparacion estricta
let esEstrictamenteIgual = 10 === 10; // === es el operador de comparacion estricta, devuelve un valor booleano
let esEstrictamenteDiferente = 10 !== 10; // !== es el operador de comparacion estricta, devuelve un valor booleano

//comparacion mayor y menor
let esMayor = 20 > 10; // > es el operador de comparacion mayor, devuelve un valor booleano
let esMenor = 5 < 10; // < es el operador de comparacion menor, devuelve un valor booleano
let esMayorOIgual = 10 >= 10; // >= es el operador de comparacion mayor o igual, devuelve un valor booleano
let esMenorOIgual = 10 <= 10; // <= es el operador de comparacion menor o igual, devuelve un valor booleano

//operadores logicos
(10 > 5) && (10 < 20); // && es el operador logico AND, devuelve un valor booleano
(10 > 5) || (10 < 20); // || es el operador logico OR, devuelve un valor booleano
!(10 > 5); // ! es el operador logico NOT, devuelve un valor booleano

//concatenacion
let nombre = "Esteban";
let apellido = "Garcia";
let nombreCompleto = nombre + " " + apellido; // + es el operador de concatenacion


