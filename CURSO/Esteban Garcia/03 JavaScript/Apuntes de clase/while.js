//ciclo while
let contador = 0; 
while (contador < 10) {
    console.log(`contador: ${contador}`);
    contador++;
}

//ciclo do while
//ejecuta al menos una vez el codigo y luego evalua la condicion   
let contador2 = 0;
do {
    console.log(`contador2: ${contador2}`);
    contador2++;
} while (contador2 < 10);